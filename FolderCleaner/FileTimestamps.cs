namespace FolderCleaning
{
    /// <summary>
    /// File available timestamps.
    /// </summary>
    public enum FileTimestamps
    {
        /// <summary>
        /// Date of the file creation.
        /// </summary>
        Creation,

        /// <summary>
        /// Date of the last access of the file.
        /// </summary>
        LastAccess,

        /// <summary>
        /// Date of the last write of the file.
        /// </summary>
        LastWrite
    }
}